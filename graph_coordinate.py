def find_path(graph, start, end, path=[]):
      path.append(start)
      if start == end:
          return path
      if start not in graph:
          return None
      for node in graph:
          last_element = path[-1]
          if node[0] > last_element[0] and node[1] > last_element[1]:
              path.append(node)
      return path

